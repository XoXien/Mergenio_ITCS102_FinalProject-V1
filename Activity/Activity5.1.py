#Arithmetic Operators
#Addition (+)
x=10  
y=10
z = x + y
#Output
print(z)

#Subtraction (-)
z = x - y
#Output
print(z)

#Multiplication (*)
z = x * y
#Output
print(z)

#Division (/)
z = x / y
#Output
print(z)

#Modulus
#Remainder (Ex. 6.21 = 21)
z = x % y
#Output
print(z)

#Exponentiation (**)
#Exponent
z = x ** y
#Output
print(z)

# Floor division (//)
# Integer or Base Number (Ex. 6.21 = 6) 
z = x // y
#Output
print(z)

